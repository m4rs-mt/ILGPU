namespace ILGPU.Runtime.Velocity
{
    /// <summary>
    /// The base class for all velocity parameters.
    /// </summary>
    abstract class VelocityParameters
    {
        /// <summary>
        /// Does nothing at the moment
        /// </summary>
        protected VelocityParameters() { }

        /// <summary>
        /// Dumps all parameters to the default console for debugging purposes.
        /// </summary>
        public abstract void DumpToConsole();
    }
}

